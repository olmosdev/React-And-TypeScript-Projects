// Conditional Statements

const availableBalance = 4000;
const balanceToWithdraw = 200;

if(availableBalance >= balanceToWithdraw) {
    console.log("Processing...");
} else {
    console.log("Insufficient Balance");
}

if(!(availableBalance >= balanceToWithdraw)) {
    console.log("Processing...");
} else {
    console.log("Insufficient Balance");
}


