// With "LET", you can change the type of any variable
let something = "Uwu";
console.log(something);
something = 99;
console.log(something);

// And you can initialize a variable without a value
let message;
console.log(message); // "Undefined" (a JS type) refers to an existing variable, which was initialized but did't get a value 
message = "XD";
console.log(message);